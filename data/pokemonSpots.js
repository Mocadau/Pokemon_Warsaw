function buildAssetPaths(name, imageExtension, ballSlug) {
  const pokemonBase = `/assets/Pokemons/${name}`;
  const ballAnimation = BALL_ANIMATION_PATHS[ballSlug] || BALL_ANIMATION_PATHS['poke-ball'];

  return {
    folder: pokemonBase,
    locationImage: `${pokemonBase}/${name}.${imageExtension}`,
    pixel: `${pokemonBase}/pixel/${name}.png`,
    pixelFallback: `${pokemonBase}/${name}.png`,
    intro: `${pokemonBase}/spawn${name}.mp4`,
    throw: `${pokemonBase}/throw${name}.mp4`,
    pokeballWaiting: ballAnimation.waiting,
    pokeballCatch: ballAnimation.catch,
    pokeballEscape: ballAnimation.escape,
  };
}

function createSpot(config) {
  const {
    id,
    dexNumber,
    name,
    imageExtension,
    ballSlug,
    lat,
    lng,
    locationName,
    description,
    dexEntry,
    species,
    type,
    category,
    height,
    weight,
    ability,
    habitat,
    weakness,
    difficulty,
    allowReplay = true,
    image,
    collectionSprite,
    silhouette,
    spawnVideo = null,
    throwVideo = null,
    introVideo = null,
    captureVideo = null,
    successVideo = null,
    failVideo = null,
  } = config;

  const assetPaths = buildAssetPaths(name, imageExtension, ballSlug);

  return {
    id,
    dexNumber,
    name,
    species,
    description,
    dexEntry,
    lat,
    lng,
    locationName,
    image: image || assetPaths.pixel,
    silhouette: silhouette || assetPaths.pixel,
    collectionSprite: collectionSprite || image || assetPaths.pixel,
    fallbackImage: assetPaths.locationImage,
    pixelFallback: assetPaths.pixelFallback,
    spawnVideo: spawnVideo || assetPaths.intro,
    throwVideo: throwVideo || assetPaths.throw,
    introVideo: introVideo || assetPaths.intro,
    captureVideo: captureVideo || assetPaths.pokeballWaiting,
    successVideo: successVideo || assetPaths.pokeballCatch,
    failVideo: failVideo || assetPaths.pokeballEscape,
    type,
    category,
    height,
    weight,
    ability,
    habitat,
    weakness,
    difficulty,
    allowReplay,
    pokeball: BALL_LABELS[ballSlug],
    pokeballSlug: ballSlug,
    assetPaths,
  };
}

